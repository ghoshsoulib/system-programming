;CONVERT CHARACTER  FROM UPPERCASE TO LOWERCASE
.MODEL SMALL
.STACK 300H
.DATA
	MSG DB "ENTER CHARACTER $"
.CODE
MAIN PROC

MOV AX,@DATA
MOV DS,AX

LEA DX,MSG ;DISPLAY INPUT PROMPT MSG
MOV AH,09H
INT 21H

MOV AH,01H  ;ACCEPT CHARACTER
INT 21H

CMP AL,'a'
JNC UPPERCASE

;CMP AL,'A'
;JL UPPERCASE

;CMP AL,'Z'
;JG UPPERCASE

ADD AL,20H

UPPERCASE:
	MOV DL,AL ;DISPLAY  CONVERTED CHARACTER
	MOV AH,02H
	INT 21H

MOV AH,4CH
INT 21H

MAIN ENDP
END MAIN
